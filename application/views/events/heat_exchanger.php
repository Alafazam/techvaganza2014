			<div class="e_text">
				<h2>Device Specifications : </h2>
				<ul>
					<li> The exchanger to be designed will be semi-batch type (unsteady state), in which the whole of the hot water (2.5kg) will be filled in beginning, and cold water is supplied continuously. The heat transfer type will be indirect contact. </li>

<li>Your prototype must fit in a box of dimensions 30cm* 40cm* 40 cm. </li>

<li> The prototype should not be moved during the course of operation. </li>
<li> The prototype must have a separate provision for entry and exit of hot and cold fluids, and water supply will be provided through a pipe of diameter ½ inches. </li>

<li> The prototype will be designed for capacity of 2.5 kg of hot water. </li>
<li> No external source of energy or work should cross the control volume (where heat transfer is taking place) of prototype. </li>
				</ul>
			</div>
			<div class="e_text">
				<h2>Game Rules : </h2>
				<ul>
					<li>All the participating teams will be provided with 2.5 kg of hot water, and a funnel will be provided for the purpose of filling hot water into exchanger. The bore of the funnel used will be ½. </li>
<li>If the difference between the mass of hot water fed and recovered at the end of the process exceeds by 40 grams, the team will be disqualified. Also, a proper mechanism must be provided for filling and withdrawing hot water in 
the device. </li>
<li>Tap water will be used as the source of cold water, and during the entire run the volumetric flow rate will be kept constant for all teams. </li>

<li>The teams will be asked to get ready 15 minutes before the start of their competition.</li> 

<li>Note that only one trial run will be given for the teams around the competition arena. </li>

<li>The run time will be declared at the start of the event. </li>

				</ul>
			</div>
