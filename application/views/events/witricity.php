				<h2>Idea : </h2>
				<p>Transfer of power from one point without wires can
achieved by the Magneting Coupling of two coils,
one transmitting coil and other receiving coil.When an
alternating current is passed through the primary coil,
it produces a pulsating flux.When this varying flux links
with the secondary coil, it produces an alternating EMF
hence an alternating current in it.</p>
			</div>
			<div class="e_text">
        	    
				<h2>Task : </h2>
				<p>The main task that the participants have to perform in this event
is to design the secondary coil .The participants have to consider
an array of factors during the design so that there is maximum
transfer of power from primary to the secondary.<br>
The participants have to choose that design which permits
maximum flux linkage in the secondary coil while using the
as little wire as possible.<br>
Following Empirical Formula would be used to judge the
designs :<br>
Di/L
Where :<br>
Di is the distance in cm from the primary at
which 1 Volt is induced in the secondary .<br>
L is the length of wire used.<br>
24 AWG winding wire and material for frame will
be provided by the organisers.
</p>
